  ILI9341_DrawText("DT05! GO TEAM!",FONT4,10,150,BLACK,WHITE);
  HAL_Delay(2000);
  ILI9341_DrawImage(Josue, SCREEN_VERTICAL_1);
  HAL_Delay(2000);
  //ILI9341_DrawImage(Connor, SCREEN_VERTICAL_1);
  //HAL_Delay(2000);
